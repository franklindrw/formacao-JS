// == (comparação implicita) compara somente o valor idependente do tipo

const numero = 5;
const texto = "5";

console.log("omparação implicita");
console.log(numero == texto);

// === (comparação explicita) compara o valor e o tipo de dado
console.log("comparação explicita");
console.log(numero === texto);

// tipeof
console.log(typeof numero);
console.log(typeof texto);
